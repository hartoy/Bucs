<p>STADIUM RENOVATIONS 2016-2018
FOLLOWING THE COMPLETION OF THE INITIAL <span>PHASE OF A $160-PLUS MILLION MULTI-PHASE RENOVATION IN 2016</span> , RAYMOND JAMES STADIUM NOW BOASTS THE MOST TECHNOLOGICALLY ADVANCED HD VIDEO SYSTEM IN USE TODAY. IT IS COMPRISED OF TWO MASSIVE 9,600 SQUARE-FOOT BOARDS ON THE NORTH AND SOUTH ENDS OF THE STADIUM – AND FOUR LARGE 2,300 SQUARE-FOOT HD TOWER WALLS. WITH MORE THAN 31,000 SQUARE FEET OF VIDEO DISPLAY SPACE, THIS SYSTEM RANKS AS THE THIRD-LARGEST VIDEO BOARD SYSTEM IN THE NFL. THE NEW SOUND SYSTEM HAS 400+ SPEAKERS THROUGHOUT THE STADIUM PROVIDING 750,000 WATTS OF POWER AND HAS OVER 30 MILES OF CABLE CONNECTING THE SPEAKERS. THE NEW VIDEO BOARDS AND SOUND SYSTEM TAKES A  FAN’S GAME EXPERIENCE TO A WHOLE NEW LEVEL.

